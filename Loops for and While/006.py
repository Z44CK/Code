# Faça um programa que, dado um conjunto de N números, determine o menor valor, o maior valor e a soma dos valores.

continuar = True

n1 = int(input('Informe um numero: '))

maior = n1
menor = n1
soma = n1

while continuar:

    n2 = int(input('Informe um numero (digite 0 para parar): '))

    if n2 == 0:
        continuar = False
    else:
        if n2 > maior:
            maior = n2
        elif n2 < menor:
            menor = n2

        soma = (n1 + n2)

        print(f'O menor valor é {menor} e o maior valor é {maior}!!')
        print(f'A soma dos valores informados é {soma}!!')
